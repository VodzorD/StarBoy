#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list **alst, void (*del)(void *, size_t))
{
	if (alst)
	{
		del(*alst, sizeof(*alst));
		free(*alst);
		*alst = NULL;
	}
}
